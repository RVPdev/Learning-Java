package notes;

public class OperatorsUse {
    public static void main(String[] args) {
        int number1 = 12;
        int number2 = 6; // if u divide an int by and int the result will always will be an int
        // better use division with floats or dobules

        number1 = number1 + 5;
        number2 += 5; // this is called assingment operator
        // increasing values of a declared variable (can be use with other operators
        // like +, -, *, /, %)

        System.out.println(number1 / number2);
        System.out.println(number1);
    }

}
